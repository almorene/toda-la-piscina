#include<unistd.h>

void	ft_putnbr(int nb)
{
	char	a[10];
	int		n;

	n = 0;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb - 1;
	}
	while (nb != 0)
	{
		a[n] = (nb % 10) + '0';
		nb = nb / 10;
		n++;
	}
	while (n >= 0)
	{
		write (1, &a[n - 1], 1);
		n--;
	}
}
